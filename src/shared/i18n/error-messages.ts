function toDictionary(locale: "zh-CN" | "en-US"): MessageDictionary {
  const source = locale === "zh-CN" ? zh : en;
  const dict: MessageDictionary = {};
  for (const [code, message] of Object.entries(source) as [ErrorCode, string][]) {
    dict[`error.${code}`] = message;
  }
  return dict;
}

export const errorMessageDictionaries = {
  "zh-CN": toDictionary("zh-CN"),
  "en-US": toDictionary("en-US")
} as const;
